__host__ __device__ unsigned int hash(unsigned int a) {
  a = (a + 0x7ed55d16) + (a << 12);
  a = (a ^ 0xc761c23c) ^ (a >> 19);
  a = (a + 0x165667b1) + (a << 5);
  a = (a + 0xd3a2646c) ^ (a << 9);
  a = (a + 0xfd7046c5) + (a << 3);
  a = (a ^ 0xb55a4f09) ^ (a >> 16);
  return a;
}

/**
* LOOK-1.2 - this is a typical helper function for a CUDA kernel.
* Function for generating a random vec3.
*/
__host__ __device__ glm::vec3 generateRandomVec3(float time, int index) {
  thrust::default_random_engine rng(hash((int)(index * time)));
  thrust::uniform_real_distribution<float> unitDistrib(-1, 1);

  return glm::vec3((float)unitDistrib(rng), (float)unitDistrib(rng), (float)unitDistrib(rng));
}

/**
* LOOK-1.2 - This is a basic CUDA kernel.
* CUDA kernel for generating boids with a specified mass randomly around the star.
*/
__global__ void kernGenerateRandomPosArray(int time, int N, glm::vec3 * arr, float scale) {
  int index = (blockIdx.x * blockDim.x) + threadIdx.x;
  if (index < N) {
    glm::vec3 rand = generateRandomVec3(time, index);
    arr[index].x = scale * rand.x;
    arr[index].y = scale * rand.y;
    arr[index].z = scale * rand.z;
  }
}

__global__ void updateVelocities(float* vel, int N) {
	int index = (blockIdx.x * blockDim.x) + threadIdx.x;
	if (index < N) {
			vel[index * 3 + 0] = 0.0f;
			vel[index * 3 + 1] = 1.0f;
			vel[index * 3 + 2] = 1.0f;
	}
}

__global__ void updateVelocities1(float* vel, int N) {
	int index = (blockIdx.x * blockDim.x) + threadIdx.x;
	if (index < N) {
		vel[index * 3 + 0] = 1.0f;
		vel[index * 3 + 1] = 0.0f;
		vel[index * 3 + 2] = 0.0f;
	}
}

/**
* Initialize memory, update some globals
*/
void Boids::initSimulation(float* x, float* y, int numX, int numY) {
  numObjects = numX + numY;
  dim3 fullBlocksPerGrid((numObjects + blockSize - 1) / blockSize);

  // LOOK-1.2 - This is basic CUDA memory management and error checking.
  // Don't forget to cudaFree in  Boids::endSimulation.
  cudaMalloc((void**)&dev_pos, numObjects * sizeof(float));
  checkCUDAErrorWithLine("cudaMalloc dev_pos failed!");

  cudaMalloc((void**)&dev_vel1, numObjects * sizeof(float));
  checkCUDAErrorWithLine("cudaMalloc dev_vel1 failed!");

  cudaMemcpy(dev_pos, x, numX * sizeof(float), cudaMemcpyHostToDevice);
  cudaMemcpy(dev_pos + numX, y, numY * sizeof(float), cudaMemcpyHostToDevice);

  dim3 fullBlocksPerGrid1((numX + blockSize - 1) / blockSize);
  updateVelocities << <fullBlocksPerGrid1, blockSize >> > (dev_vel1, numX/3);
  dim3 fullBlocksPerGrid2((numY + blockSize - 1) / blockSize);
  updateVelocities1 << <fullBlocksPerGrid2, blockSize >> > (dev_vel1 + numX, numY/3);

  cudaDeviceSynchronize();
}

void Boids::copyToDevice(float* x, float* y, int numX, int numY) {
	cudaMemcpy(dev_pos, x, numX * sizeof(float), cudaMemcpyHostToDevice);
	cudaMemcpy(dev_pos + numX, y, numY * sizeof(float), cudaMemcpyHostToDevice);
}

