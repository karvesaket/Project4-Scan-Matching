/**
* Copy the boid positions into the VBO so that they can be drawn by OpenGL.
*/
__global__ void kernCopyPositionsToVBO(int N, float *pos, float *vbo, float s_scale) {
  int index = threadIdx.x + (blockIdx.x * blockDim.x);

  float c_scale = -1.0f / s_scale;

  if (index < N) {
    vbo[4 * index + 0] = pos[index * 3 + 0] * c_scale;
    vbo[4 * index + 1] = pos[index * 3 + 1] * c_scale;
    vbo[4 * index + 2] = pos[index * 3 + 2] * c_scale;
    vbo[4 * index + 3] = 1.0f;
  }
}

__global__ void kernCopyVelocitiesToVBO(int N, float *vel, float *vbo, float s_scale) {
  int index = threadIdx.x + (blockIdx.x * blockDim.x);

  if (index < N) {
    vbo[4 * index + 0] = vel[index * 3 + 0] + 0.3f;
    vbo[4 * index + 1] = vel[index * 3 + 1] + 0.3f;
    vbo[4 * index + 2] = vel[index * 3 + 2] + 0.3f;
    vbo[4 * index + 3] = 1.0f;
  }
}

/**
* Wrapper for call to the kernCopyboidsToVBO CUDA kernel.
*/
void Boids::copyBoidsToVBO(float *vbodptr_positions, float *vbodptr_velocities) {
  dim3 fullBlocksPerGrid(((numObjects/3) + blockSize - 1) / blockSize);

  kernCopyPositionsToVBO <<< fullBlocksPerGrid, blockSize >>>(numObjects/3, dev_pos, vbodptr_positions, scene_scale);
  kernCopyVelocitiesToVBO <<< fullBlocksPerGrid, blockSize >>>(numObjects/3, dev_vel1, vbodptr_velocities, scene_scale);

  checkCUDAErrorWithLine("copyBoidsToVBO failed!");

  cudaDeviceSynchronize();
}


